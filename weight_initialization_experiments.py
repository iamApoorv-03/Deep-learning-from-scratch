# imports and data generation
import torch
import torch.nn as nn
import torch.optim as optim
import matplotlib.pyplot as plt
from sklearn.model_selection  import train_test_split

torch.manual_seed(42)
X = torch.linspace(-10,10,200).reshape(-1,1)
noise= torch.randn_like(X) * 2
true_y = 3*X**2 +2*X+5
y = true_y + noise
#normalisation
X_norm= (X-X.mean(dim=0))/ X.std(dim=0)
y_norm= (y-y.mean(dim=0))/ y.std(dim=0)

from torch.utils.data import TensorDataset , DataLoader
#train test split
X_train,X_test,y_train,y_test= train_test_split(X_norm,y_norm, test_size=0.2,random_state=42)
#creation of dataset objects
train_dataset= TensorDataset(X_train, y_train)
test_datset= TensorDataset(X_test,y_test)


# data loader creation
train_loader = DataLoader(train_dataset, batch_size= 8 ,shuffle =True )
test_loader = DataLoader(test_datset, batch_size= 8, shuffle= False)

# model creation                                     # we have to create a model creation function because :
def create_model():                                  # so we can initialise different kind of weight for every time a new model
  model = nn.Sequential(
    nn.Linear(1,64),
    nn.ReLU(),
    nn.Linear(64,64),
    nn.ReLU(),
    nn.Linear(64,64),
    nn.ReLU(),
    nn.Linear(64,1)
)
  return model

# weight initialization function

#1)small weights (vanishing gradients)
def init_small(model): # we defined a function named init_small(model) where model is our whole neural network and init_small recieves entire nn as input.
    for layer in model:   # nn.squential model behaves like a list of layers. # so this loop iterates through every layer of the network.

        if isinstance(layer, nn.Linear): # this check if the layer is linear layer or not as we have to apply initialisation to linear layers only.
           nn.init.normal_(layer.weight, mean = 0, std =0.001) # pytorch provides initialization functions in nn.init.
           # we used normal function it fills the tensor with normal distribution  values.

#2) large weights (exploding gradients)
def init_large(model):
    for layer in model:

      if isinstance(layer, nn.Linear):
        nn.init.normal_(layer.weight, mean = 0,  std = 5)

#3)  he initialization (proper weight initialization)
def init_he(model):
    for layer in model:

      if isinstance(layer, nn.Linear):
        nn.init.kaiming_normal_(layer.weight)        # we are using  kaiming_normal_ function it performs He initialization
                                                     #also Kaiming initialization = He initialization
                                                     #  this last line initializes the weights of a layer using He (Kaiming) initialization.
  #It fills the weight matrix with values drawn from a Gaussian (normal) distribution whose variance is carefully chosen to keep activations stable across layers.

"""Imagine signal flow through layers.

Without good initialization:

Layer1 → small signal

Layer2 → smaller signal

Layer3 → almost zero

With He initialization:

Layer1 → stable signal

Layer2 → stable signal

Layer3 → stable signal

This keeps forward activations and backward gradients balanced.
"""

# initialization dictionarty creation

initializations ={
    "small": init_small,
    "large": init_large,
    "he":    init_he
}
#loss
criterion = nn.MSELoss()

all_losses = {}
for name , init_fn in initializations.items():                        # items() converts dictionary into pairs.
  print(f"Training with {name}")

  model = create_model()
  init_fn(model)
  optimizer = optim.Adam(model.parameters(), lr =0.01)

  losses=[]

  for epochs in range(200):
    running_loss = 0.0
    for batch_X,batch_y in train_loader:
      predictions = model(batch_X)
      loss = criterion(predictions, batch_y)
      optimizer.zero_grad()
      loss.backward()
      optimizer.step()
      running_loss +=loss.item()
    avg_training_loss = running_loss/len(train_loader)
    losses.append(avg_training_loss)

    if epochs % 20 ==0:
      print(f"epoch:{epochs}, loss:{avg_training_loss:.4f}")

  all_losses[name]= losses

#plotting of training loss
plt.figure(figsize=(8,6))

for name , losses in all_losses.items():
  plt.plot(losses, label = name)

plt.xlabel("Epochs")
plt.ylabel("Training Losses")
plt.title("Weight initialization Comparison")
plt.legend()
plt.grid(True)
plt.show()

